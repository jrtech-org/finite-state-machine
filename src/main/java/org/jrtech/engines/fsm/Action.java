package org.jrtech.engines.fsm;

import java.io.Serializable;

/**
 * Root action defining common methods for an action.
 *
 */
public interface Action extends Serializable {
    public String getName();

    /**
     * @param processContextData
     * @return event name published according to the processing.
     */
    public String run(ProcessContext processContextData);

    public String getOutputEvent();
}